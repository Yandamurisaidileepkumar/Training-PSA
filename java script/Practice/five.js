let day = 2;
switch (day) {
    case 0:
        day = 0
        console.log("sunday");
        break;
    case 1:
        day = 1
        console.log("monday");
        break;
    case 2:
        day = 2
        console.log("tuesday");

    case 3:
        day = 3
        console.log("wednesday");

    case 4:
        day = 4
        console.log("thursday");

    case 5:
        day = 5
        console.log("friday");

    case 6:
        day = 6
        console.log("Saturday");

    default:
        console.log("day not found");


}